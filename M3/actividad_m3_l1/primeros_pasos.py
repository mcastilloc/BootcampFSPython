# --------------------------------------------
# 3. Realizar operaciones básicas
# --------------------------------------------

# Declaro dos números
numero_1 = 10
numero_2 = 20

# Sumo los dos números
resultado = numero_1 + numero_2  # Aquí realizo la operación de suma

# Muestro el resultado de la suma
print("La suma de", numero_1, "y", numero_2, "es:", resultado)

